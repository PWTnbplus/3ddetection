from __future__ import annotations

import argparse
import json
import math
import pickle
from collections import Counter, defaultdict
from pathlib import Path
from typing import Dict, Iterable, List, Optional


CLASS_NAME_MAP = {
    'car': 'car',
    'van': 'van',
    'truck': 'truck',
    'bus': 'bus',
    'pedestrian': 'pedestrian',
    'person_sitting': 'person sitting',
    'cyclist': 'cyclist',
    'bicycle': 'bicycle',
    'motor': 'motorcycle',
    'motorcycle': 'motorcycle',
    'ride_other': 'rider',
    'rider': 'rider',
    'tram': 'tram',
    'misc': 'object',
}

IGNORE_CLASSES = {'dontcare'}


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description='Generate scene text descriptions from radar detections.')
    parser.add_argument(
        '--data-root',
        default='/root/lanyun-fs/dataset/radar',
        help='Root path of the radar dataset.')
    parser.add_argument(
        '--source',
        default='prediction',
        choices=['prediction', 'label'],
        help='Use point-cloud detector predictions for the real second method, '
        'or labels only for oracle/debug experiments.')
    parser.add_argument(
        '--prediction-file',
        default=None,
        help='JSON prediction file generated by a point-cloud detector. '
        'Required when --source prediction.')
    parser.add_argument(
        '--prediction-split',
        default='val',
        help='ImageSets split used by the prediction file. It is used to map '
        'KITTI-style continuous sample indices back to original frame ids.')
    parser.add_argument(
        '--score-thr',
        type=float,
        default=0.25,
        help='Minimum detection confidence used for generated text.')
    parser.add_argument(
        '--class-names',
        nargs='+',
        default=[
            'bicycle', 'Car', 'Cyclist', 'motor', 'Pedestrian', 'ride_other',
            'truck'
        ],
        help='Class names used to map numeric prediction labels.')
    parser.add_argument(
        '--output-dir',
        default=None,
        help='Directory used to save generated text annotations. '
        'Defaults to <data-root>/texts.')
    parser.add_argument(
        '--splits',
        nargs='+',
        default=['train', 'val', 'train_val', 'full', 'test'],
        help='ImageSets splits to process.')
    return parser.parse_args()


def normalize_class_name(raw_name: str) -> str:
    key = raw_name.strip().lower()
    return CLASS_NAME_MAP.get(key, key.replace('_', ' '))


def pluralize(word: str, count: int) -> str:
    if count == 1:
        return word
    irregular = {
        'person': 'people',
        'person sitting': 'people sitting',
    }
    if word in irregular:
        return irregular[word]
    if word.endswith('y') and not word.endswith(('ay', 'ey', 'iy', 'oy', 'uy')):
        return word[:-1] + 'ies'
    if word.endswith(('s', 'x', 'z', 'ch', 'sh')):
        return word + 'es'
    return word + 's'


def format_count_phrase(count: int, class_name: str) -> str:
    return f'{count} {pluralize(class_name, count)}'


def join_phrases(phrases: List[str]) -> str:
    if not phrases:
        return ''
    if len(phrases) == 1:
        return phrases[0]
    if len(phrases) == 2:
        return f'{phrases[0]} and {phrases[1]}'
    return ', '.join(phrases[:-1]) + f', and {phrases[-1]}'


def read_split_ids(split_file: Path) -> List[str]:
    with split_file.open('r', encoding='utf-8') as f:
        return [line.strip() for line in f if line.strip()]


def parse_label_file(label_path: Path) -> List[Dict]:
    objects: List[Dict] = []
    if not label_path.exists():
        return objects

    with label_path.open('r', encoding='utf-8') as f:
        lines = [line.strip() for line in f if line.strip()]

    for line in lines:
        fields = line.split()
        if len(fields) < 15:
            continue

        raw_name = fields[0]
        if raw_name.strip().lower() in IGNORE_CLASSES:
            continue

        location_x = float(fields[11])
        location_y = float(fields[12])
        location_z = float(fields[13])
        distance = math.sqrt(location_x**2 + location_z**2)

        objects.append(
            dict(
                raw_name=raw_name,
                class_name=normalize_class_name(raw_name),
                truncated=float(fields[1]),
                occluded=int(float(fields[2])),
                alpha=float(fields[3]),
                bbox=[float(v) for v in fields[4:8]],
                dimensions_hwl=[float(v) for v in fields[8:11]],
                location_camera=[location_x, location_y, location_z],
                rotation_y=float(fields[14]),
                score=float(fields[15]) if len(fields) > 15 else None,
                distance=distance,
            ))
    return objects


def as_list(value):
    if hasattr(value, 'tolist'):
        return value.tolist()
    return value


def get_field(data: Dict, names: List[str], default=None):
    for name in names:
        if name in data:
            return data[name]
    return default


def normalize_prediction_record(record: Dict, class_names: List[str],
                                score_thr: float) -> Dict:
    sample_id = str(
        get_field(record, ['sample_id', 'image_idx', 'frame_id', 'token'], ''))
    pred = get_field(record, ['pred_instances_3d', 'predictions', 'instances'],
                     record)
    boxes = as_list(get_field(pred, ['bboxes_3d', 'boxes_3d', 'boxes'], []))
    scores = as_list(get_field(pred, ['scores_3d', 'scores', 'score'], []))
    labels = as_list(get_field(pred, ['labels_3d', 'labels', 'label'], []))

    objects = []
    for index, box in enumerate(boxes):
        score = scores[index] if index < len(scores) else 1.0
        if score < score_thr:
            continue

        raw_label = labels[index] if index < len(labels) else -1
        if isinstance(raw_label, str):
            raw_name = raw_label
        elif 0 <= int(raw_label) < len(class_names):
            raw_name = class_names[int(raw_label)]
        else:
            raw_name = f'class_{raw_label}'

        # Common LiDAR boxes are [x, y, z, dx, dy, dz, yaw].
        location_x = float(box[0])
        location_y = float(box[1]) if len(box) > 1 else 0.0
        location_z = float(box[2]) if len(box) > 2 else 0.0
        distance = math.sqrt(location_x**2 + location_y**2)
        objects.append(
            dict(
                raw_name=raw_name,
                class_name=normalize_class_name(raw_name),
                location_lidar=[location_x, location_y, location_z],
                bbox_3d=[float(v) for v in box],
                score=float(score),
                distance=distance,
            ))

    return build_record_from_objects(sample_id, objects, source='prediction')


def load_prediction_records(prediction_file: Path, class_names: List[str],
                            score_thr: float,
                            sample_id_map: Optional[List[str]] = None) -> List[Dict]:
    if prediction_file.suffix in {'.pkl', '.pickle'}:
        with prediction_file.open('rb') as f:
            raw_data = pickle.load(f)
    else:
        with prediction_file.open('r', encoding='utf-8') as f:
            raw_data = json.load(f)

    if isinstance(raw_data, dict):
        raw_data = raw_data.get('data_list', raw_data.get('results', raw_data))
        if isinstance(raw_data, dict):
            raw_data = [
                dict(sample_id=sample_id, predictions=pred)
                for sample_id, pred in raw_data.items()
            ]

    return [
        normalize_kitti_prediction_record(record, score_thr, sample_id_map)
        if 'name' in record and 'location' in record else
        normalize_prediction_record(record, class_names, score_thr)
        for record in raw_data
    ]


def normalize_sample_id(sample_idx, sample_id_map: Optional[List[str]] = None) -> str:
    sample_ids = as_list(sample_idx)
    if isinstance(sample_ids, list):
        sample_idx = sample_ids[0] if sample_ids else ''
    else:
        sample_idx = sample_ids

    try:
        sample_index = int(sample_idx)
    except (TypeError, ValueError):
        return str(sample_idx)

    if sample_id_map is not None and 0 <= sample_index < len(sample_id_map):
        return sample_id_map[sample_index]
    return str(sample_index)


def normalize_kitti_prediction_record(record: Dict, score_thr: float,
                                      sample_id_map: Optional[List[str]] = None) -> Dict:
    sample_id = normalize_sample_id(record.get('sample_idx', ''), sample_id_map)
    names = as_list(record.get('name', []))
    locations = as_list(record.get('location', []))
    dimensions = as_list(record.get('dimensions', []))
    rotations = as_list(record.get('rotation_y', []))
    scores = as_list(record.get('score', []))

    objects = []
    for index, name in enumerate(names):
        score = scores[index] if index < len(scores) else 1.0
        if score < score_thr:
            continue

        location = locations[index]
        location_x = float(location[0])
        location_y = float(location[1]) if len(location) > 1 else 0.0
        location_z = float(location[2]) if len(location) > 2 else 0.0
        distance = math.sqrt(location_x**2 + location_z**2)
        objects.append(
            dict(
                raw_name=str(name),
                class_name=normalize_class_name(str(name)),
                location_camera=[location_x, location_y, location_z],
                dimensions_lhw=[float(v) for v in dimensions[index]]
                if index < len(dimensions) else None,
                rotation_y=float(rotations[index])
                if index < len(rotations) else None,
                score=float(score),
                distance=distance,
            ))

    return build_record_from_objects(sample_id, objects, source='prediction')


def get_distance_bucket(distance: float) -> str:
    if distance < 15:
        return 'near'
    if distance < 35:
        return 'mid-range'
    return 'far'


def get_direction_bucket(location_x: float, location_z: float) -> str:
    if location_z < 0:
        longitudinal = 'behind'
    elif location_z < 8:
        longitudinal = 'very close ahead'
    else:
        longitudinal = 'ahead'

    if location_x < -3:
        lateral = 'left'
    elif location_x > 3:
        lateral = 'right'
    else:
        lateral = 'center'

    if lateral == 'center':
        return longitudinal
    if longitudinal == 'behind':
        return f'behind on the {lateral}'
    return f'{longitudinal} on the {lateral}'


def get_lidar_direction_bucket(location_x: float, location_y: float) -> str:
    if location_x < 0:
        longitudinal = 'behind'
    elif location_x < 8:
        longitudinal = 'very close ahead'
    else:
        longitudinal = 'ahead'

    if location_y > 3:
        lateral = 'left'
    elif location_y < -3:
        lateral = 'right'
    else:
        lateral = 'center'

    if lateral == 'center':
        return longitudinal
    if longitudinal == 'behind':
        return f'behind on the {lateral}'
    return f'{longitudinal} on the {lateral}'


def get_object_direction(obj: Dict) -> str:
    if 'location_lidar' in obj:
        x, y, _ = obj['location_lidar']
        return get_lidar_direction_bucket(x, y)
    x, _, z = obj['location_camera']
    return get_direction_bucket(x, z)


def summarize_objects(objects: List[Dict]) -> str:
    if not objects:
        return 'No detected objects are present in this scene.'

    class_counter = Counter(obj['class_name'] for obj in objects)
    class_phrases = [
        format_count_phrase(count, class_name)
        for class_name, count in sorted(class_counter.items())
    ]

    grouped_by_direction: Dict[str, Counter] = defaultdict(Counter)
    for obj in objects:
        grouped_by_direction[get_object_direction(obj)][obj['class_name']] += 1

    direction_phrases = []
    for direction, counter in sorted(grouped_by_direction.items()):
        pieces = [
            format_count_phrase(count, class_name)
            for class_name, count in sorted(counter.items())
        ]
        direction_phrases.append(f'{join_phrases(pieces)} {direction}')

    nearest_obj = min(objects, key=lambda obj: obj['distance'])
    nearest_phrase = (
        f'The closest target is a {nearest_obj["class_name"]} '
        f'at about {nearest_obj["distance"]:.1f} meters.'
    )

    distance_counter = Counter(get_distance_bucket(obj['distance']) for obj in objects)
    distance_order = ['near', 'mid-range', 'far']
    distance_phrases = [
        f'{distance_counter[bucket]} {bucket}'
        for bucket in distance_order
        if distance_counter[bucket] > 0
    ]

    return (
        f'This driving scene contains {join_phrases(class_phrases)}. '
        f'Most labeled objects are located as follows: '
        f'{join_phrases(direction_phrases)}. '
        f'Object distances are distributed as {join_phrases(distance_phrases)}. '
        f'{nearest_phrase}'
    )


def build_record_from_objects(sample_id: str,
                              objects: List[Dict],
                              source: str) -> Dict:
    class_counter = Counter(obj['class_name'] for obj in objects)
    direction_counter = Counter()
    for obj in objects:
        direction_counter[get_object_direction(obj)] += 1

    return dict(
        sample_id=sample_id,
        source=source,
        text=summarize_objects(objects),
        num_objects=len(objects),
        counts_by_class=dict(sorted(class_counter.items())),
        counts_by_direction=dict(sorted(direction_counter.items())),
        objects=objects,
    )


def build_record(sample_id: str, label_path: Path) -> Dict:
    objects = parse_label_file(label_path)
    return build_record_from_objects(sample_id, objects, source='label_oracle')


def dump_json(data: Iterable[Dict], output_path: Path) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with output_path.open('w', encoding='utf-8') as f:
        json.dump(list(data), f, indent=2, ensure_ascii=False)


def main() -> None:
    args = parse_args()
    data_root = Path(args.data_root)
    output_dir = Path(args.output_dir) if args.output_dir else data_root / 'texts'
    image_sets_dir = data_root / 'ImageSets'
    label_dir = data_root / 'training' / 'label_2'

    if args.source == 'prediction':
        if args.prediction_file is None:
            raise ValueError(
                '--prediction-file is required when --source prediction. '
                'Run a point-cloud detector first and export its predictions.')
        sample_id_map = None
        split_file = image_sets_dir / f'{args.prediction_split}.txt'
        if split_file.exists():
            sample_id_map = read_split_ids(split_file)
        records = load_prediction_records(
            Path(args.prediction_file), args.class_names, args.score_thr,
            sample_id_map)
        output_path = output_dir / 'radar_texts_prediction.json'
        dump_json(records, output_path)
        print(f'Saved {len(records)} prediction-based records to {output_path}')
        return

    for split in args.splits:
        split_file = image_sets_dir / f'{split}.txt'
        if not split_file.exists():
            print(f'Skip split "{split}" because {split_file} does not exist.')
            continue

        sample_ids = read_split_ids(split_file)
        records = [
            build_record(sample_id, label_dir / f'{sample_id}.txt')
            for sample_id in sample_ids
        ]
        output_path = output_dir / f'radar_texts_{split}.json'
        dump_json(records, output_path)
        print(f'Saved {len(records)} records to {output_path}')


if __name__ == '__main__':
    main()
