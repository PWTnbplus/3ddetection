from __future__ import annotations

import argparse
import json
import os
import time
import urllib.error
import urllib.request
from pathlib import Path
from typing import Dict, Iterable, List, Optional


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description='Generate radar scene text with DeepSeek chat API.')
    parser.add_argument(
        '--input-file',
        default='/root/lanyun-fs/dataset/radar/texts/radar_llm_prompts_prediction.jsonl',
        help='Input JSONL file generated by radar_llm_prompt_converter.py.')
    parser.add_argument(
        '--output-file',
        default='/root/lanyun-fs/dataset/radar/texts/radar_llm_texts_prediction.json',
        help='Output JSON file for LLM generated text.')
    parser.add_argument(
        '--api-key',
        default=None,
        help='DeepSeek API key. Defaults to DEEPSEEK_API_KEY env var.')
    parser.add_argument(
        '--base-url',
        default='https://api.deepseek.com',
        help='DeepSeek OpenAI-compatible API base URL.')
    parser.add_argument(
        '--model',
        default='deepseek-chat',
        help='DeepSeek model name.')
    parser.add_argument(
        '--temperature',
        type=float,
        default=0.2,
        help='Sampling temperature.')
    parser.add_argument(
        '--max-tokens',
        type=int,
        default=96,
        help='Maximum output tokens for each scene description.')
    parser.add_argument(
        '--timeout',
        type=int,
        default=60,
        help='HTTP timeout in seconds.')
    parser.add_argument(
        '--sleep',
        type=float,
        default=0.2,
        help='Sleep seconds between API calls.')
    parser.add_argument(
        '--max-samples',
        type=int,
        default=None,
        help='Only process the first N samples, useful for testing.')
    parser.add_argument(
        '--resume',
        action='store_true',
        help='Skip sample ids already present in the output JSON.')
    return parser.parse_args()


def load_jsonl(path: Path) -> List[Dict]:
    records = []
    with path.open('r', encoding='utf-8') as f:
        for line in f:
            line = line.strip()
            if line:
                records.append(json.loads(line))
    return records


def load_existing(path: Path) -> List[Dict]:
    if not path.exists():
        return []
    with path.open('r', encoding='utf-8') as f:
        return json.load(f)


def dump_json(records: Iterable[Dict], path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open('w', encoding='utf-8') as f:
        json.dump(list(records), f, indent=2, ensure_ascii=False)


def build_payload(record: Dict, model: str, temperature: float,
                  max_tokens: int) -> Dict:
    return dict(
        model=model,
        messages=[
            dict(role='system', content=record['system_prompt']),
            dict(role='user', content=record['user_prompt']),
        ],
        temperature=temperature,
        max_tokens=max_tokens,
    )


def call_deepseek(record: Dict, api_key: str, base_url: str, model: str,
                  temperature: float, max_tokens: int, timeout: int) -> str:
    url = base_url.rstrip('/') + '/chat/completions'
    payload = build_payload(record, model, temperature, max_tokens)
    data = json.dumps(payload).encode('utf-8')
    request = urllib.request.Request(
        url,
        data=data,
        headers={
            'Authorization': f'Bearer {api_key}',
            'Content-Type': 'application/json',
        },
        method='POST')

    try:
        with urllib.request.urlopen(request, timeout=timeout) as response:
            response_data = json.loads(response.read().decode('utf-8'))
    except urllib.error.HTTPError as error:
        detail = error.read().decode('utf-8', errors='replace')
        raise RuntimeError(f'DeepSeek HTTP {error.code}: {detail}') from error

    return response_data['choices'][0]['message']['content'].strip()


def build_output_record(prompt_record: Dict, text: str) -> Dict:
    return dict(
        sample_id=prompt_record.get('sample_id', ''),
        text=text,
        source='llm_deepseek_prediction',
        template_text=prompt_record.get('template_text', ''),
        metadata=prompt_record.get('metadata', {}))


def main() -> None:
    args = parse_args()
    api_key = args.api_key or os.environ.get('DEEPSEEK_API_KEY')
    if not api_key:
        raise ValueError(
            'DeepSeek API key is required. Pass --api-key or set '
            'DEEPSEEK_API_KEY.')

    input_path = Path(args.input_file)
    output_path = Path(args.output_file)
    prompt_records = load_jsonl(input_path)
    if args.max_samples is not None:
        prompt_records = prompt_records[:args.max_samples]

    output_records = load_existing(output_path) if args.resume else []
    done_ids = {record.get('sample_id') for record in output_records}

    total = len(prompt_records)
    for index, prompt_record in enumerate(prompt_records, 1):
        sample_id = prompt_record.get('sample_id', '')
        if args.resume and sample_id in done_ids:
            continue

        text = call_deepseek(
            prompt_record,
            api_key=api_key,
            base_url=args.base_url,
            model=args.model,
            temperature=args.temperature,
            max_tokens=args.max_tokens,
            timeout=args.timeout)
        output_records.append(build_output_record(prompt_record, text))
        dump_json(output_records, output_path)
        print(f'[{index}/{total}] {sample_id}: {text}')
        if args.sleep > 0:
            time.sleep(args.sleep)

    print(f'Saved {len(output_records)} LLM generated records to {output_path}')


if __name__ == '__main__':
    main()
